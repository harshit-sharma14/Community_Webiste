// backend/models/User.js
const mongoose = require('mongoose');


// Schema for User model
const userSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    minlength: 3,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    match: /.+\@.+\..+/,
  },
  password: {
    type: String,
    required: true,
    minlength: 6,
  },
  
}, 
  
);


module.exports = mongoose.model('users', userSchema);
